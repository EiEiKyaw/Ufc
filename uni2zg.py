# -*- coding: utf-8 -*-
import re


def convert(input):

    output = input

    output = output.replace(u'\u103A', u'\u1039') # AThet...
    output = output.replace(u'\u103B', u'\u103A') # YaPint...
    output = output.replace(u'\u103C', u'\u103B') # YaYint...
    output = output.replace(u'\u103D', u'\u103C') # WaSwe...
    output = output.replace(u'\u103E', u'\u103D') # HaHtoe...

#-----------------------------------------------------------Replace----------------------------------------------------------------------


    output = re.sub(u'([\u1000-\u1021])(\u1031)', u"\\2\\1", output)  # Byee...TheWayHtoe
    output = re.sub(u'([\u103A\u103B\u103C\u103D])(\u1031)', u"\\2\\1", output)  # Byee...TheWayHtoe
    #output = re.sub(u'([\u1000\u1003\u1006\u1009\u100A\u100F\u1011\u1010\u1018\u101A\u101C\u101E\u101F\u1021])(\u103B)', u"\\2\\1" , output)  # Byee...YaYint...
    output = re.sub(u'([\u1001\u1002\u1004\u1005\u1007\u1008\u100B\u100C\u100D\u100E\u1012\u1013\u1014\u1015\u1016\u1017\u1019\u101B\u101D])(\u103B)', u"\\2\\1", output)  # Byee...YaYint...
    #output = output.replace(u'\u103B[\u1000\u1003\u1006\u1009\u100A\u100F\u1011\u1010\u1018\u101A\u101C\u101E\u101F\u1021]', u'\u107E[\u1000\u1003\u1006\u1009\u100A\u100F\u1011\u1010\u1018\u101A\u101C\u101E\u101F\u1021]')  # Byee...YaYint...
    output = re.sub(u'([\u1000-\u1021])(\u1031)([\u103A\u103C\u103D])', u"\\2\\1\\3", output)  # Byee...PSH...TWH
    output = re.sub(u'([\u1000-\u1021])(\u1031)([\u103B])', u"\\2\\3\\1", output)  # Byee...Y...TWH
    output = re.sub(u'[\u1000-\u1021]\u102F', u"\\1\\2", output)  # Byee...Y...TCN
    output = output.replace(u'\u102F', u'\u102F')  # TCN...
    output = re.sub(u'(\u103B[\u1000-\u1021])(\u102F)', u"\\1\\2\\3", output)  # Byee...Y...TCN
    output = output.replace(u'\u102F', u'\u1033')  # TCN...


    return output
