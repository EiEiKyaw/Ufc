def visual2logical(input):
    output = input
    output = re.sub(u'([\u1021-\u1023])((?:\u100B)?)((?:\u100C)?)((?:\u100D)?)((?:\u100E)?)((?:\u1031)?)', '\\6\\3\\1\\2\\4\\5') # ThaWayHtoe...Pint/Yit/Swe/Htoe...
    return output

def convert(input):
    output = input

    output = replace(output)
    output = shape(output)
    output = decompose(output)
    output = visual2logical(output)

    return output
